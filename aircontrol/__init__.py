"""AirControl — мультимодальная бесконтактная система управления ПК.

Слияние трёх модальностей ввода:
  * жесты рук (MediaPipe Hand Landmarker),
  * движение руки (позиционирование курсора + фильтры стабилизации),
  * голос (распознавание речи + команды).

Пакет разбит на независимые модули, чтобы каждую подсистему можно было
тестировать и оценивать отдельно (важно для экспериментальной части работы):

    config      — центральная конфигурация (dataclasses + JSON).
    platform    — кросс-платформенный слой ОС (приложения, громкость, скриншот).
    tracking    — захват камеры, обёртка MediaPipe, фильтры стабилизации курсора.
    gestures    — извлечение признаков, эвристический и ML-распознаватели жестов.
    voice       — распознавание речи и голосовые команды.
    fusion      — координатор слияния модальностей (жесты + голос).
    evaluation  — стенд оценки по закону Фиттса (ISO 9241-9) и телеметрия.
    ui          — overlay-окно, HUD, мастер калибровки.
    app         — главный оркестратор.
"""

